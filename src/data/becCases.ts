// ── Date helper — shifts timestamps to be relative to today ──────────────────
const _ANCHOR_MS = new Date('2024-05-01').getTime()
const _OFFSET_MS = Date.now() - _ANCHOR_MS
function d(s: string): string {
  const [datePart, timePart] = s.split(' ')
  const shifted = new Date(new Date(datePart).getTime() + _OFFSET_MS)
  const y  = shifted.getFullYear()
  const m  = String(shifted.getMonth() + 1).padStart(2, '0')
  const dy = String(shifted.getDate()).padStart(2, '0')
  return timePart ? `${y}-${m}-${dy} ${timePart}` : `${y}-${m}-${dy}`
}


